public class Punkt {

    //Declare variables
    private double x, y;

    // Create a constructor
    public Punkt( double x, double y ) {
        this.x = x;
        this.y = y;
    }

    // Create getter for x
    public double getX() {
        return x;
    }

    // Create getter for y
    public double getY() {
        return y;
    }
}